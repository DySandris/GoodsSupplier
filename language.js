  // Language Switch Function
  function changeLanguage() {
    const selectedLang = document.getElementById('languageDropdown').value;
  
    if (translations[selectedLang]) {
      const keys = Object.keys(translations[selectedLang]);
      keys.forEach(key => {
        const element = document.getElementById(key);
        if (element) {
          element.innerText = translations[selectedLang][key];
        }
      });
    } else {
      console.error("Translation not found for:", selectedLang);
    }
  }
  